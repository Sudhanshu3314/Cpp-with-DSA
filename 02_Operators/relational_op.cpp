#include<bits/stdc++.h>
using namespace std;

int main(){

    int a = 5 ;
    int b = 9 ;

    cout << ( a == b ) << endl ; // false 
    cout << ( a != b ) << endl ; // true 
    cout << ( a < b ) << endl ; // true 
    cout << ( a >= b ) << endl ; // false 

    return 0;
}