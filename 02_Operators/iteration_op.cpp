#include<bits/stdc++.h>
using namespace std;

int main(){

    int c = 6 ;
    cout << c++ << endl; // 6
    cout << c << endl ; // 7 
    cout << ++c << endl ; //  8
    cout << c-- << endl ; // 8 
    cout << --c << endl ; // 6 

    return 0;
}