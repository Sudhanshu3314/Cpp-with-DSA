#include<bits/stdc++.h>
using namespace std;

int main(){

    int a = 6 , b = 9 ;

    a += b ; // a = a + b
    cout << "a = " << a << endl ; // 15
    b -= 5 ; // b = b - 5
    cout << "b = " << b << endl ; // 4 
    return 0;
}